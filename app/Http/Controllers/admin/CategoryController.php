<?php

namespace App\Http\Controllers\Admin;

use App\Http\Controllers\Controller;
use App\Http\Requests\Admin\CategoryRequest;
use App\Models\Category;
use Illuminate\Http\Request;
use Illuminate\Support\Facades\Storage;

class CategoryController extends Controller
{
    public function index(Request $request)
{
    $sortBy = $request->input('sort_by', 'created_at');
    $sortDir = $request->input('sort_dir', 'desc');

    $categories = Category::query()
        ->when($request->filled('search'), function ($query) use ($request) {
            $query->where('name', 'like', "%{$request->search}%");
        })
        ->when($request->filled('status'), function ($query) use ($request) {
            $query->where('is_active', $request->status);
        })
        ->with(['parent']) // Load quan h·ªá
        ->withCount('products') // üëà Th√™m d√≤ng n√†y
        ->orderBy($sortBy, $sortDir)
        ->paginate(10);

    return view('admin.categories.index', compact('categories', 'sortBy', 'sortDir'));
}

    public function create()
    {
        $categories = Category::active()->get();
        return view('admin.categories.create', compact('categories'));
    }

    public function show(Category $category)
    {
        return view('admin.categories.show', compact('category'));
    }

    public function store(CategoryRequest $request)
    {
        try {
            $data = $request->validated();

            // X·ª≠ l√Ω upload ·∫£nh
            if ($request->hasFile('icon')) {
                $data['icon'] = $request->file('icon')->store('categories', 'public');
            }

            Category::create($data);

            return redirect()
                ->route('admin.categories.index')
                ->with('success', 'Th√™m danh m·ª•c th√†nh c√¥ng');
        } catch (\Exception $e) {
            return redirect()
                ->back()
                ->withInput()
                ->with('error', 'ƒê√£ c√≥ l·ªói x·∫£y ra: ' . $e->getMessage());
        }
    }

    public function edit(Category $category)
    {
        $categories = Category::active()
            ->where('id', '!=', $category->id)
            ->get();
        return view('admin.categories.edit', compact('category', 'categories'));
    }

    public function update(CategoryRequest $request, Category $category)
    {
        try {
            $data = $request->validated();

            // X·ª≠ l√Ω upload ·∫£nh m·ªõi
            if ($request->hasFile('icon')) {
                // X√≥a ·∫£nh c≈© n·∫øu c√≥
                if ($category->icon) {
                    Storage::disk('public')->delete($category->icon);
                }
                $data['icon'] = $request->file('icon')->store('categories', 'public');
            }

            $category->update($data);

            return redirect()
                ->route('admin.categories.index')
                ->with('success', 'C·∫≠p nh·∫≠t danh m·ª•c th√†nh c√¥ng');
        } catch (\Exception $e) {
            return redirect()
                ->back()
                ->withInput()
                ->with('error', 'ƒê√£ c√≥ l·ªói x·∫£y ra: ' . $e->getMessage());
        }
    }

    public function destroy(Category $category)
