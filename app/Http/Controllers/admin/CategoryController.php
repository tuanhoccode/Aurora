                'success' => 'C·∫≠p nh·∫≠t tr·∫°ng th√°i c√°c danh m·ª•c ƒë√£ ch·ªçn th√†nh c√¥ng'
            ]);
        } catch (\Exception $e) {
            return response()->json([
                'error' => 'ƒê√£ c√≥ l·ªói x·∫£y ra: ' . $e->getMessage()
            ], 500);
        }
    }
