                'error' => 'ƒê√£ c√≥ l·ªói x·∫£y ra: ' . $e->getMessage()
            ], 500);
        }
    }

    // Th√™m ph∆∞∆°ng th·ª©c bulk toggle status
    public function bulkToggle(Request $request)
    {
        try {
            $ids = $request->input('ids', []);
            if (empty($ids)) {
                return response()->json([
                    'error' => 'Vui l√≤ng ch·ªçn √≠t nh·∫•t m·ªôt danh m·ª•c'
                ], 400);
            }

            $status = $request->input('status');
            if (!isset($status)) {
                return response()->json([
                    'error' => 'Tr·∫°ng th√°i kh√¥ng h·ª£p l·ªá'
                ], 400);
            }

            Category::whereIn('id', $ids)->update(['is_active' => $status]);

