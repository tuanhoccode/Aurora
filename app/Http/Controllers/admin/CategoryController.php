    try {
        // üëá Kh√¥ng cho x√≥a n·∫øu c√≥ s·∫£n ph·∫©m
        if ($category->products()->count() > 0) {
            return redirect()
                ->back()
                ->with('error', 'Kh√¥ng th·ªÉ x√≥a v√¨ danh m·ª•c ƒëang ch·ª©a s·∫£n ph·∫©m.');
        }

        $category->delete();

        return redirect()
            ->route('admin.categories.index')
            ->with('success', 'ƒê√£ chuy·ªÉn danh m·ª•c v√†o th√πng r√°c');
    } catch (\Exception $e) {
        return redirect()
            ->back()
            ->with('error', 'ƒê√£ c√≥ l·ªói x·∫£y ra: ' . $e->getMessage());
    }
}


    public function trash(Request $request)
    {
        // L·∫•y tham s·ªë s·∫Øp x·∫øp t·ª´ request
        $sortBy = $request->input('sort_by', 'deleted_at'); // M·∫∑c ƒë·ªãnh s·∫Øp x·∫øp theo ng√†y x√≥a
        $sortDir = $request->input('sort_dir', 'desc'); // M·∫∑c ƒë·ªãnh s·∫Øp x·∫øp gi·∫£m d·∫ßn

        $trashedCategories = Category::onlyTrashed()
            ->when($request->filled('search'), function ($query) use ($request) {
                $query->where('name', 'like', "%{$request->search}%");
            })
            ->when($request->filled('status'), function ($query) use ($request) {
                $query->where('is_active', $request->status);
            })
            ->orderBy($sortBy, $sortDir)
            ->paginate(10);

        return view('admin.categories.trash', compact('trashedCategories', 'sortBy', 'sortDir'));
    }

    public function restore($id)
    {
        try {
            $category = Category::onlyTrashed()->findOrFail($id);
            $category->restore();

            return redirect()
                ->route('admin.categories.trash')
                ->with('success', 'Kh√¥i ph·ª•c danh m·ª•c th√†nh c√¥ng');
        } catch (\Exception $e) {
            return redirect()
                ->back()
                ->with('error', 'ƒê√£ c√≥ l·ªói x·∫£y ra: ' . $e->getMessage());
        }
    }

    public function forceDelete($id)
    {
        try {
            $category = Category::onlyTrashed()->findOrFail($id);

            // X√≥a ·∫£nh n·∫øu c√≥
            if ($category->icon) {
                Storage::disk('public')->delete($category->icon);
            }

            $category->forceDelete();

            return redirect()
                ->route('admin.categories.trash')
                ->with('success', 'X√≥a danh m·ª•c vƒ©nh vi·ªÖn th√†nh c√¥ng');
        } catch (\Exception $e) {
            return redirect()
                ->back()
                ->with('error', 'ƒê√£ c√≥ l·ªói x·∫£y ra: ' . $e->getMessage());
        }
    }

    public function bulkDelete(Request $request)
{
    try {
        $ids = $request->input('ids', []);
        if (empty($ids)) {
            return response()->json([
                'error' => 'Vui l√≤ng ch·ªçn √≠t nh·∫•t m·ªôt danh m·ª•c'
            ], 400);
        }

        // L·∫•y danh m·ª•c c√≥ ƒë·∫øm s·ªë s·∫£n ph·∫©m
        $categories = Category::withCount('products')
            ->whereIn('id', $ids)
            ->get();

        // Danh m·ª•c c√≥ th·ªÉ x√≥a (kh√¥ng c√≥ s·∫£n ph·∫©m)
        $deletable = $categories->filter(fn($cat) => $cat->products_count == 0);

        // Danh m·ª•c kh√¥ng th·ªÉ x√≥a (c√≥ s·∫£n ph·∫©m)
        $nonDeletable = $categories->filter(fn($cat) => $cat->products_count > 0);

        // X√≥a c√°c danh m·ª•c kh√¥ng c√≥ s·∫£n ph·∫©m
        foreach ($deletable as $category) {
            $category->delete();
        }

        return response()->json([
            'success' => $deletable->isNotEmpty()
                ? 'ƒê√£ chuy·ªÉn c√°c danh m·ª•c kh√¥ng ch·ª©a s·∫£n ph·∫©m v√†o th√πng r√°c.'
                : null,
            'warning' => $nonDeletable->isNotEmpty()
                ? 'M·ªôt s·ªë danh m·ª•c kh√¥ng th·ªÉ x√≥a v√¨ ƒëang ch·ª©a s·∫£n ph·∫©m: ' . $nonDeletable->pluck('name')->join(', ')
                : null,
            'deleted_ids' => $deletable->pluck('id'),
        ]);
    } catch (\Exception $e) {
        return response()->json([
            'error' => 'ƒê√£ c√≥ l·ªói x·∫£y ra: ' . $e->getMessage()
        ], 500);
    }
}


    public function bulkForceDelete(Request $request)
    {
        try {
            $ids = $request->input('ids', []);
            if (empty($ids)) {
                return response()->json([
                    'error' => 'Vui l√≤ng ch·ªçn √≠t nh·∫•t m·ªôt th∆∞∆°ng hi·ªáu'
                ], 400);
            }

            $categories = Category::onlyTrashed()->whereIn('id', $ids)->get();

            foreach ($categories as $category) {
                if ($category->icon) {
                    Storage::disk('public')->delete($category->icon);
                }
                $category->forceDelete();
            }

            return response()->json([
                'success' => 'ƒê√£ x√≥a vƒ©nh vi·ªÖn c√°c th∆∞∆°ng hi·ªáu ƒë√£ ch·ªçn'
            ]);
        } catch (\Exception $e) {
            return response()->json([
                'error' => 'ƒê√£ c√≥ l·ªói x·∫£y ra: ' . $e->getMessage()
            ], 500);
        }
    }

    public function bulkRestore(Request $request)
    {
